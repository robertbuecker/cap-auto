def write_cap_csv(fn: str, ds: List[dict]) -> None:
    """Write experiment results in CrysAlisPro ED result viewer CSV format.
    
    Args:
        fn: Output filename
        ds: List of experiment dicts (all must have same keys)
    
    Example:
        >>> experiments = [{'Experiment name': 'exp1', 'Cell': '10 10 10 90 90 90'}, ...]
        >>> write_cap_csv('results.csv', experiments)
    """
    from textwrap import dedent
    
    with open(fn, 'w', newline='') as fh:
        fh.write(dedent(
            f'''\
            VERSION 1
            HEADER INFO: 
            Number of experiments: {len(ds)}
            Number of columns {len(ds[0])}



            '''
        ))
        writer = csv.DictWriter(fh, fieldnames=ds[0].keys())
        writer.writeheader()
        for experiment in ds:
            writer.writerow(experiment)


def parse_cap_csv(fn: str, use_raw_cell: bool,
                  filter_missing: bool = True) -> Tuple[List[dict], np.ndarray, np.ndarray]:
    """Parse a CSV experiment report file from the CrysAlisPro ED result viewer.
    
    Args:
        fn: Path to CSV file
        use_raw_cell: If True, use 'Current unit cell'; if False, use 'Final SG unit cell'
        filter_missing: If True, filter out experiments with empty cell parameters
    
    Returns:
        Tuple of (experiments, cells, centrings):
            experiments: List of experiment dicts
            cells: Numpy array of cell parameters (N x 6: a, b, c, α, β, γ)
            centrings: Numpy array of centering symbols ('P', 'I', 'F', etc.)
    
    Example:
        >>> exps, cells, centrings = parse_cap_csv('results.csv', use_raw_cell=True)
        >>> print(f"Found {len(exps)} experiments")
        >>> print(f"Cell a-axes: {cells[:, 0]}")
    """
    with open(fn) as fh:
        for _ in range(7):
            _ = fh.readline()
        ds = list(csv.DictReader(fh))
    
    key = 'Current unit cell' if use_raw_cell else 'Final SG unit cell'
    ds = [d for d in ds if d.get(key, '').strip()] if filter_missing else ds
    cells = np.array([d[key].split() for d in ds]).astype(float)
    
    def get_centring(d: dict) -> str:
        if use_raw_cell:
            ctr = d.get('Current lattice', 'P').strip()[-1]
        else:
            ctr = d.get('Space group RED', 'P').strip()[0]
        if ctr not in ['P', 'I', 'F', 'A', 'B', 'C', 'R', 'H']:
            warnings.warn(f'Unknown centring {ctr}. Assuming P')
            ctr = 'P'
        return ctr
    
    centrings = np.array([get_centring(d) for d in ds])
    
    return ds, cells, centrings


