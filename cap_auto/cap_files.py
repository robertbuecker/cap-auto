class DiffractionData:
    """Container and analyzer for diffraction data from reflections and powder patterns.
    
    This class provides methods to compute shell statistics and assign shells to
    diffraction data, useful for resolution-dependent analysis.
    
    Args:
        reflections: List of reflection dicts from parse_reflection_table()
        powder: List of powder pattern dicts from parse_powder_pattern()
        wavelength: X-ray wavelength in Angstroms (default: 0.0251 for 200keV electrons)
    
    Example:
        >>> reflections, _ = parse_reflection_table('exp.tab')
        >>> powder, _ = parse_powder_pattern('radial.dat')
        >>> diff_data = DiffractionData(reflections, powder)
        >>> shells = diff_data.compute_shell_statistics()
        >>> for shell in shells:
        ...     print(f"Shell {shell['d_min']:.2f}-{shell['d_max']:.2f} Å: "
        ...           f"{shell['N_peaks']} peaks, ratio={shell['peak_ratio']:.3f}")
    """
    
    def __init__(self, reflections: List[Dict[str, Any]], 
                 powder: List[Dict[str, Any]], 
                 wavelength: float = 0.0251):
        self.reflections = reflections
        self.powder = powder
        self.wavelength = wavelength
        self._shell_boundaries = None
    
    def _suggest_shell_boundaries(self) -> List[float]:
        """Suggest sensible shell boundaries based on data range.
        
        Returns shells in inverse d-spacing (1/Å) from low to high resolution.
        Default strategy: [1/d_max, 1/10, 1/1.2, 1/0.8, 1/(d_min*1.5), 1/d_min]
        
        Returns:
            List of shell boundaries in inverse Angstroms
        """
        if not self.powder:
            return [0.0, 0.1, 0.833, 1.25, 2.0]  # Fallback boundaries
        
        d_values = [p['d_value'] for p in self.powder if p['d_value'] > 0]
        if not d_values:
            return [0.0, 0.1, 0.833, 1.25, 2.0]
        
        d_min = min(d_values)
        d_max = max(d_values)
        
        # Create sensible shell boundaries
        boundaries = [
            1 / d_max,           # Lowest resolution
            1 / 10.0,            # ~10 Å shell
            1 / 1.2,             # ~1.2 Å shell
            1 / 0.8,             # ~0.8 Å shell
            1 / (d_min * 1.5),   # High resolution buffer
            1 / d_min            # Highest resolution
        ]
        
        # Remove duplicates and sort
        boundaries = sorted(list(set(b for b in boundaries if b >= 0)))
        
        return boundaries
    
    def assign_shells(self, shell_boundaries: Optional[List[float]] = None,
                     in_place: bool = True) -> Tuple[List[Dict[str, Any]], List[Dict[str, Any]]]:
        """Assign shell indices to reflections and powder data.
        
        Args:
            shell_boundaries: List of boundaries in 1/d (Å⁻¹). If None, auto-suggest.
            in_place: If True, modify self.reflections and self.powder; if False, return copies
        
        Returns:
            Tuple of (reflections_with_shells, powder_with_shells)
        """
        if shell_boundaries is None:
            shell_boundaries = self._suggest_shell_boundaries()
        
        self._shell_boundaries = shell_boundaries
        
        # Work on copies if not in_place
        reflections = self.reflections if in_place else [r.copy() for r in self.reflections]
        powder = self.powder if in_place else [p.copy() for p in self.powder]
        
        # Assign shells to reflections
        for refl in reflections:
            inv_d = refl.get('inv_d', 0)
            # Find shell index using digitize logic
            shell_idx = 0
            for i, boundary in enumerate(shell_boundaries[:-1]):
                if boundary <= inv_d < shell_boundaries[i + 1]:
                    shell_idx = i
                    break
            else:
                shell_idx = len(shell_boundaries) - 2 if inv_d >= shell_boundaries[-1] else 0
            refl['shell'] = shell_idx
        
        # Assign shells to powder data
        for point in powder:
            inv_d = point.get('inv_d', 0)
            shell_idx = 0
            for i, boundary in enumerate(shell_boundaries[:-1]):
                if boundary <= inv_d < shell_boundaries[i + 1]:
                    shell_idx = i
                    break
            else:
                shell_idx = len(shell_boundaries) - 2 if inv_d >= shell_boundaries[-1] else 0
            point['shell'] = shell_idx
        
        return reflections, powder
    
    def compute_shell_statistics(self, shell_boundaries: Optional[List[float]] = None) -> List[Dict[str, Any]]:
        """Compute per-shell statistics from reflections and powder data.
        
        Args:
            shell_boundaries: List of boundaries in 1/d (Å⁻¹). If None, auto-suggest.
        
        Returns:
            List of dicts with shell statistics:
                - d_max: Maximum d-spacing in shell (Å)
                - d_min: Minimum d-spacing in shell (Å)
                - I_tot: Total intensity in powder pattern
                - I_peak: Total intensity in reflections
                - N_peaks: Number of reflections
                - peak_ratio: I_peak / I_tot (fraction of intensity in peaks)
        
        Example:
            >>> shells = diff_data.compute_shell_statistics()
            >>> high_res_shell = shells[-1]
            >>> print(f"Highest resolution shell: {high_res_shell['d_min']:.2f} Å")
            >>> print(f"Peak/powder ratio: {high_res_shell['peak_ratio']:.3f}")
        """
        if shell_boundaries is None:
            shell_boundaries = self._suggest_shell_boundaries()
        
        # Assign shells first
        self.assign_shells(shell_boundaries, in_place=True)
        
        shell_stats = []
        
        for shell_idx in range(len(shell_boundaries) - 1):
            # Get d-spacing range for this shell
            inv_d_min = shell_boundaries[shell_idx]
            inv_d_max = shell_boundaries[shell_idx + 1]
            d_max = 1 / inv_d_min if inv_d_min > 0 else float('inf')
            d_min = 1 / inv_d_max if inv_d_max > 0 else 0
            
            # Compute statistics for reflections in this shell
            shell_reflections = [r for r in self.reflections if r.get('shell') == shell_idx]
            I_peak = sum(r.get('I', 0) for r in shell_reflections)
            N_peaks = len(shell_reflections)
            
            # Compute statistics for powder data in this shell
            shell_powder = [p for p in self.powder if p.get('shell') == shell_idx]
            I_tot = sum(p.get('intensity', 0) for p in shell_powder)
            
            # Compute peak ratio
            peak_ratio = I_peak / I_tot if I_tot > 0 else float('nan')
            
            shell_stats.append({
                'd_max': d_max,
                'd_min': d_min,
                'I_tot': I_tot,
                'I_peak': I_peak,
                'N_peaks': N_peaks,
                'peak_ratio': peak_ratio
            })
        
        return shell_stats


