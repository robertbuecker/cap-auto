def get_diff_info(path: str, cap: Optional['CAPInstance'] = None,
                  keep_peak_file: bool = False, keep_powder_file: bool = False,
                  redo_peak_hunt: bool = True, wavelength: float = 0.0251,
                  pow_dmin: float = 0.3, pow_dmax: float = 20.0,
                  recenter_pattern: bool = True,
                  shell_boundaries: Optional[List[float]] = None,
                  log: Optional[Callable[[str], None]] = None) -> Tuple[List[Dict], List[Dict], List[Dict], str]:
    """Extract diffraction information from a CAP experiment.
    
    This function orchestrates the extraction of peak tables, powder patterns,
    and shell statistics. It can optionally generate missing files via CAP commands.
    
    Args:
        path: Path to experiment (without .par extension)
        cap: CAPInstance for generating files. If None, only parse existing files.
        keep_peak_file: Don't regenerate peak file if it exists
        keep_powder_file: Don't regenerate powder file if it exists
        redo_peak_hunt: Re-run peak hunting before writing table
        wavelength: X-ray wavelength in Angstroms
        pow_dmin: Minimum d-spacing for powder pattern (Å)
        pow_dmax: Maximum d-spacing for powder pattern (Å)
        recenter_pattern: Adjust center before extracting powder pattern
        shell_boundaries: Custom shell boundaries in 1/d. If None, auto-suggest.
        log: Logging function (default: print)
    
    Returns:
        Tuple of (shell_statistics, reflections, powder_data, diff_image_path):
            shell_statistics: List of dicts with per-shell statistics
            reflections: List of dicts with reflection data
            powder_data: List of dicts with powder pattern data
            diff_image_path: Path to diffraction image snapshot
    
    Example:
        >>> from cap_auto.cap_control import CAPInstance
        >>> cap = CAPInstance(start_now=True)
        >>> shells, peaks, powder, img = get_diff_info('exp_11317', cap=cap)
        >>> print(f"Found {len(peaks)} reflections")
        >>> print(f"Shell statistics: {len(shells)} shells")
        >>> cap.stop()
    """
    if log is None:
        log = print
    
    powder_fn = os.path.join(os.path.dirname(path), 'radial.dat')
    peak_fn = path + '.tab'
    
    # Determine what needs to be generated
    generate_powder = (not os.path.exists(powder_fn)) or (not keep_powder_file)
    generate_peaks = (not os.path.exists(peak_fn)) or (not keep_peak_file)
    
    # Generate files via CAP if needed and if CAP instance provided
    if cap is not None and (generate_powder or generate_peaks):
        from cap_auto.cap_control import CAPInstance  # Import here to avoid circular dependency
        
        if not isinstance(cap, CAPInstance):
            raise TypeError("cap must be a CAPInstance")
        
        cmds = []
        
        if recenter_pattern and (generate_powder or generate_peaks):
            cmds.append('dc microedadjustcenter')
        
        if generate_powder:
            if os.path.exists(powder_fn):
                log(f"Removing existing powder file: {powder_fn}")
                os.remove(powder_fn)
            cmds.extend(generate_powder_commands(path, wavelength, pow_dmin, pow_dmax, recenter=False))
        
        if generate_peaks:
            if os.path.exists(peak_fn):
                log(f"Removing existing peak file: {peak_fn}")
                os.remove(peak_fn)
            cmds.extend(generate_reflection_commands(path, redo_peak_hunt))
        
        # Execute commands via CAP
        if cmds:
            log(f"Generating files via CAP: {len(cmds)} commands")
            for cmd in cmds:
                result = cap.execute(cmd, timeout=180)
                if not result.success:
                    log(f"Warning: Command failed: {cmd}")
            
            # Wait for files to appear
            max_wait = 20  # seconds
            for fn in [powder_fn, peak_fn]:
                if fn == powder_fn and not generate_powder:
                    continue
                if fn == peak_fn and not generate_peaks:
                    continue
                
                waited = 0
                while not os.path.exists(fn) and waited < max_wait:
                    sleep(0.1)
                    waited += 0.1
                
                if not os.path.exists(fn):
                    log(f"Warning: File not created after {max_wait}s: {fn}")
    
    # Parse files (will return empty lists with warnings if files don't exist)
    reflections, _ = parse_reflection_table(peak_fn, wavelength)
    powder, _ = parse_powder_pattern(powder_fn)
    
    log(f"Parsed {len(reflections)} reflections and {len(powder)} powder points")
    
    # Compute shell statistics
    diff_data = DiffractionData(reflections, powder, wavelength)
    shell_stats = diff_data.compute_shell_statistics(shell_boundaries)
    
    # Generate diffraction image if CAP available
    diff_img_fn = path + '_diff_screen.png'
    if cap is not None:
        try:
            img_cmds, diff_img_fn = generate_diff_image_commands(path)
            for cmd in img_cmds:
                cap.execute(cmd, timeout=30)
        except Exception as e:
            log(f"Could not generate diffraction image: {e}")
    
    return shell_stats, reflections, powder, diff_img_fn


