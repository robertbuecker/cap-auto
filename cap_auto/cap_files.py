def parse_reflection_table(filename: str, wavelength: float = 0.0251) -> Tuple[List[Dict[str, Any]], List[str]]:
    """Parse a reflection table file (.tab) from CrysAlisPro.
    
    The reflection table is generated by the CAP command:
        WD OLDASCIIT <experiment_path>
    
    This writes peak positions and intensities in a format compatible with the DOS KM4 system.
    
    Args:
        filename: Path to the .tab file
        wavelength: X-ray wavelength in Angstroms (default: 0.0251 for 200keV electrons)
    
    Returns:
        Tuple of (reflections, commands_to_generate):
            reflections: List of dicts with keys:
                - x, y, z: Reciprocal space coordinates
                - R: Reciprocal resolution  
                - I: Intensity
                - centroidx, centroidy: Pixel coordinates on detector
                - d: d-spacing (Angstroms)
                - inv_d: 1/d (inverse Angstroms)
            commands_to_generate: List of CAP commands needed if file is missing
    
    Example:
        >>> reflections, cmds = parse_reflection_table('exp_11317.tab')
        >>> print(f"Found {len(reflections)} reflections")
        >>> print(f"First reflection d-spacing: {reflections[0]['d']:.3f} Å")
    """
    commands_to_generate = ['wd oldasciit "<experiment_path>"']
    
    if not os.path.exists(filename):
        warnings.warn(f"Reflection table file not found: {filename}", UserWarning)
        return [], commands_to_generate
    
    try:
        with open(filename, 'r') as f:
            # Skip header line
            header = f.readline().strip().split()
            
            reflections = []
            for line in f:
                parts = line.strip().split()
                if len(parts) < 11:  # Minimum required columns
                    continue
                
                try:
                    # Parse key columns (indices match the file format)
                    refl = {
                        'x': float(parts[0]),
                        'y': float(parts[1]),
                        'z': float(parts[2]),
                        'R': float(parts[3]),  # Reciprocal resolution
                        'I': float(parts[4]),  # Intensity
                        'centroidx': float(parts[9]),
                        'centroidy': float(parts[10]),
                    }
                    
                    # Compute derived values
                    refl['inv_d'] = refl['R'] / wavelength if wavelength > 0 else 0
                    refl['d'] = 1 / refl['inv_d'] if refl['inv_d'] > 0 else 0
                    
                    reflections.append(refl)
                except (ValueError, IndexError):
                    continue  # Skip malformed lines
        
        return reflections, commands_to_generate
    
    except Exception as e:
        warnings.warn(f"Error parsing reflection table {filename}: {e}", UserWarning)
        return [], commands_to_generate


def parse_powder_pattern(filename: str) -> Tuple[List[Dict[str, Any]], List[str]]:
    """Parse a powder diffraction pattern file (.dat) from CrysAlisPro.
    
    The powder pattern is generated by the CAP command:
        POWDER RADIAL <bins> <2theta_min> <2theta_max> <azim_min> <azim_max> <output_name>
    
    For example:
        powder radial 128 0.07 15.0 0 360 radial
    
    Args:
        filename: Path to the .dat file
    
    Returns:
        Tuple of (powder_data, commands_to_generate):
            powder_data: List of dicts with keys:
                - two_theta: 2θ angle in degrees
                - d_value: d-spacing in Angstroms
                - intensity: Integrated intensity
                - sigma: Standard deviation
                - count: Number of pixels contributing
                - inv_d: 1/d (inverse Angstroms) [computed]
            commands_to_generate: List of CAP commands needed if file is missing
    
    Example:
        >>> powder, cmds = parse_powder_pattern('radial.dat')
        >>> print(f"Powder pattern has {len(powder)} data points")
        >>> print(f"d-spacing range: {powder[0]['d_value']:.2f} - {powder[-1]['d_value']:.2f} Å")
    """
    commands_to_generate = [
        'powder radial 128 <2theta_min_deg> <2theta_max_deg> 0 360 radial'
    ]
    
    if not os.path.exists(filename):
        warnings.warn(f"Powder pattern file not found: {filename}", UserWarning)
        return [], commands_to_generate
    
    try:
        powder_data = []
        
        with open(filename, 'r') as f:
            # Skip header lines
            f.readline()  # [PowderExtract1dNew 1]
            headers = f.readline().strip().split('\t')
            
            for line in f:
                parts = line.strip().split()
                if len(parts) < 5:
                    continue
                
                try:
                    point = {
                        'two_theta': float(parts[0]),
                        'd_value': float(parts[1]),
                        'intensity': float(parts[2]),
                        'sigma': float(parts[3]),
                        'count': int(parts[4]),
                    }
                    
                    # Compute inverse d-spacing
                    point['inv_d'] = 1 / point['d_value'] if point['d_value'] > 0 else 0
                    
                    powder_data.append(point)
                except (ValueError, IndexError):
                    continue
        
        return powder_data, commands_to_generate
    
    except Exception as e:
        warnings.warn(f"Error parsing powder pattern {filename}: {e}", UserWarning)
        return [], commands_to_generate


