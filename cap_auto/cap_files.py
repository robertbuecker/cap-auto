def generate_powder_commands(exp_path: str, wavelength: float = 0.0251,
                            d_min: float = 0.3, d_max: float = 20.0,
                            recenter: bool = True) -> List[str]:
    """Generate CAP commands to create a powder diffraction pattern.
    
    Args:
        exp_path: Path to experiment (without .par extension)
        wavelength: X-ray wavelength in Angstroms
        d_min: Minimum d-spacing in Angstroms
        d_max: Maximum d-spacing in Angstroms
        recenter: Whether to adjust center before extraction
    
    Returns:
        List of CAP commands to execute
    """
    # Convert d-spacing to 2theta in degrees
    two_theta_min = wavelength / d_max * 180 / np.pi
    two_theta_max = wavelength / d_min * 180 / np.pi
    
    cmds = []
    if recenter:
        cmds.append('dc microedadjustcenter')
    
    cmds.append(f'powder radial 128 {two_theta_min:.4f} {two_theta_max:.4f} 0 360 radial')
    
    return cmds


def generate_reflection_commands(exp_path: str, redo_peak_hunt: bool = True) -> List[str]:
    """Generate CAP commands to create a reflection table.
    
    Args:
        exp_path: Path to experiment (without .par extension)
        redo_peak_hunt: Whether to re-run peak hunting before writing
    
    Returns:
        List of CAP commands to execute
    """
    cmds = []
    
    if redo_peak_hunt:
        # Peak hunt with standard parameters
        cmds.append('ph snogui_pars 1000 20 1 0 2 2 10 10 1 0 0 0 0.0 1000.0 0 1 1 1')
    
    # Write peak table in DOS-compatible ASCII format
    cmds.append(f'wd oldasciit "{exp_path}"')
    
    return cmds


def generate_diff_image_commands(exp_path: str) -> Tuple[List[str], str]:
    """Generate CAP commands to create a diffraction image snapshot.
    
    Args:
        exp_path: Path to experiment (without .par extension)
    
    Returns:
        Tuple of (commands, output_filename)
    """
    exp_dir = os.path.dirname(exp_path)
    output_fn = exp_path + '_diff_screen.png'
    
    cmds = []
    
    # Try to find middle frame
    try:
        frame_list = glob(os.path.join(exp_dir, 'frames', '*.rodhypix'))
        if frame_list:
            frame_list.sort(key=lambda fn: int(os.path.splitext(fn)[0].rsplit('_', 1)[-1]))
            middle_frame = frame_list[len(frame_list) // 2]
            cmds.append(f'rd i "{middle_frame}"')
    except Exception:
        pass  # Will use current frame
    
    cmds.append(f'wd pnggiftiff "{output_fn}"')
    
    return cmds, output_fn


