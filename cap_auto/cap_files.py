class ProffitXML:
    """Handler for CrysAlisPro data reduction (proffit) XML parameter files.
    
    These files are used with the commands:
        DC PROFFIT XML <xml_file>           # Run data reduction with XML settings
        XX PARTOXML <pars_file> [<xml_file>]  # Convert .proffitpars to XML
    
    Args:
        filename: Path to XML file
        path: Experiment path (without extension) for input/output settings
        allow_missing: If True, allow creation without existing file
        parse: If True, parse file immediately
    
    Example:
        >>> prof = ProffitXML('exp_11317.xml', 'exp_11317')
        >>> prof.set_parameters(d_min=0.8, laue=5, gral_mode=1)
        >>> # Now run: cap.execute(f'dc proffit xml {prof.filename}')
    """
    
    def __init__(self, filename: str, path: str,
                 allow_missing: bool = False, parse: bool = True):
        self.filename = filename
        self.path = path
        self.full_path = os.path.join(path, filename) if path else filename
        self.tree = None
        if parse:
            self.update(allow_missing)
    
    def update(self, allow_missing: bool = False) -> None:
        """Reload parameters from XML file."""
        try:
            self.tree = ET.parse(self.full_path)
        except FileNotFoundError:
            if allow_missing:
                self.tree = None
            else:
                raise FileNotFoundError(f'Proffit XML file {self.full_path} does not exist')
    
    def set_parameters(self, template: Optional[str] = None,
                      laue: Optional[int] = None,
                      friedel_mates: Optional[bool] = None,
                      scan_width: Optional[float] = None,
                      d_min: Optional[float] = None,
                      d_max: Optional[float] = None,
                      gral_mode: Optional[int] = None,
                      autochem: Optional[bool] = None,
                      **kwargs: str) -> None:
        """Set proffit parameters.
        
        Args:
            template: Path to template XML file to copy from
            laue: Laue group index (0-18)
            friedel_mates: Enable Friedel mate treatment
            scan_width: Scan width in degrees
            d_min: Minimum d-spacing for processing
            d_max: Maximum d-spacing for processing
            gral_mode: GRAL mode (0=OFF, 1=ONAUTO, 2=MANUAL)
            autochem: Enable AutoChem structure solution
            **kwargs: Additional XML path/value pairs as 'xpath': 'value'
        """
        if template is not None:
            if os.path.exists(template):
                tree = ET.parse(template)
            else:
                raise FileNotFoundError(f'Template file {template} does not exist')
        elif self.tree is not None:
            tree = self.tree
        else:
            raise ValueError('No parameters loaded; please specify a template')
        
        root = tree.getroot()
        
        # Set parameters
        pars = dict(kwargs)
        
        if laue is not None:
            pars['__PROFFIT__PARAMETERS__/__Type_of_Laue__'] = str(laue)
        if friedel_mates is not None:
            pars['__PROFFIT__PARAMETERS__/__Is_Friedel_mate_on__'] = '1' if friedel_mates else '0'
        if scan_width is not None:
            pars['__PROFFIT__PARAMETERS__/__Scan_width_in_deg__'] = str(scan_width)
        if d_min is not None:
            pars['__PROFFIT__PARAMETERS__/__dmin__'] = str(d_min)
        if d_max is not None:
            pars['__PROFFIT__PARAMETERS__/__dmax__'] = str(d_max)
        if gral_mode is not None:
            pars['__PROFFIT__PARAMETERS__/__Gral_mode__'] = str(gral_mode)
        if autochem is not None:
            pars['__PROFFIT__PARAMETERS__/__Is_AUTOCHEM__'] = '1' if autochem else '0'
        
        # Apply all parameters
        for xpath, value in pars.items():
            elem = root.find(xpath)
            if elem is not None:
                elem.text = value
            else:
                warnings.warn(f'XML path not found: {xpath}')
        
        self.tree = tree
        
        if self.filename:
            tree.write(self.full_path)


class FinalizationXML:
    """Handler for CrysAlisPro finalization XML parameter files.
    
    These files are used with the commands:
        DC RRPFROMXML <xml_file>           # Finalize using XML settings
        DC XMLRRP [input] [output_xml]     # Save finalization dialog to XML
    
    Args:
        filename: Path to XML file
        path: Experiment path (without extension) for input/output settings
        allow_missing: If True, allow creation without existing file
        parse: If True, parse file immediately
    
    Example:
        >>> fin = FinalizationXML('exp_11317_finalizer.xml', 'exp_11317')
        >>> fin.set_parameters(gral=True, res_limit=0.7, N_shells=15)
        >>> # Now run: cap.execute(f'dc rrpfromxml {fin.filename}')
    """
    
    def __init__(self, filename: str, path: str,
                 allow_missing: bool = False, parse: bool = True):
        self.filename = filename
        self.path = path
        self.full_path = os.path.join(os.path.dirname(path), filename)
        self.tree = None
        if parse:
            self.update(allow_missing)
    
    def update(self, allow_missing: bool = False) -> None:
        """Reload parameters from XML file."""
        try:
            self.tree = ET.parse(self.full_path)
        except FileNotFoundError:
            if allow_missing:
                self.tree = None
            else:
                raise FileNotFoundError(f'Finalization XML file {self.full_path} does not exist')
    
    def set_parameters(self, template: Optional[str] = None,
                      gral: Optional[bool] = None,
                      gral_interactive: Optional[bool] = None,
                      autochem: Optional[bool] = None,
                      laue: Optional[Union[int, str]] = None,
                      z: Optional[float] = None,
                      chem: Optional[str] = None,
                      res_limit: Optional[float] = None,
                      fom: Tuple[str, ...] = ('Rint', 'Rurim', 'Rpim', 'CC 1/2', 'deltaCC',
                                              'Sigma', 'SigmaA', 'SigmaB', 'CC*'),
                      N_shells: int = 10,
                      pars: Optional[Dict[str, str]] = None) -> None:
        """Set finalization parameters.
        
        Args:
            template: Path to template XML file to copy from
            gral: Enable GRAL space group determination
            gral_interactive: Use interactive GRAL mode (not recommended for automation)
            autochem: Enable AutoChem structure solution
            laue: Laue group (int index or string name)
            z: Number of formula units in unit cell
            chem: Chemical formula
            res_limit: High resolution limit (Angstroms)
            fom: Tuple of figure-of-merit names for output
            N_shells: Number of resolution shells for statistics
            pars: Additional XML path/value pairs
        """
        if template is not None:
            if os.path.exists(template):
                tree = ET.parse(template)
            else:
                raise FileNotFoundError(f'Template file {template} does not exist')
        elif self.tree is not None:
            tree = self.tree
        else:
            raise ValueError('No parameters loaded; please specify a template')
        
        root = tree.getroot()
        
        # Set file paths
        root.find('__FINALIZER_SAMPLE__/__Input_file__').text = os.path.basename(self.path)
        root.find('__FINALIZER_SAMPLE__/__Input_file_path__').text = os.path.dirname(self.path)
        root.find('__FINALIZER_OUTPUT__/__Output_file__').text = os.path.basename(self.path)
        root.find('__FINALIZER_OUTPUT__/__Output_file_path__').text = self.path
        
        pars = pars or {}
        
        if gral is not None:
            pars['__FINALIZER_SPACE_GROUP_AND_AUTOCHEM__/__Is_GRAL_on__'] = '1' if gral else '0'
        if gral_interactive is not None:
            pars['__FINALIZER_SPACE_GROUP_AND_AUTOCHEM__/__GRAL_mode__'] = '1' if gral_interactive else '0'
        if autochem is not None:
            pars['__FINALIZER_SPACE_GROUP_AND_AUTOCHEM__/__Is_AutoChem_active__'] = '1' if autochem else '0'
        if laue is not None:
            if isinstance(laue, str):
                # Parse laue index from indexinfo
                indexinfo = root.find('__FINALIZER_SAMPLE__/__Type_of_Laue__indexinfo__')
                if indexinfo is not None and indexinfo.text:
                    laue_map = {v.strip(): int(k) for k, v in 
                               (entry.split('-', 1) for entry in indexinfo.text.split(';'))}
                    laue = laue_map.get(laue, 0)
            pars['__FINALIZER_SAMPLE__/__Type_of_Laue__'] = str(laue)
        if res_limit is not None:
            pars['__FINALIZER_FILTERS_AND_LIMITS__/__Automated__'] = '0'
            pars['__FINALIZER_FILTERS_AND_LIMITS__/__Apply_resolution_limits__'] = '1'
            pars['__FINALIZER_FILTERS_AND_LIMITS__/__Resolution_limits_-_high_limit__'] = str(res_limit)
            pars['__FINALIZER_FILTERS_AND_LIMITS__/__Dmin_for_completness__'] = str(res_limit)
        if z is not None:
            pars['__FINALIZER_SAMPLE__/__Z__'] = str(z)
        if chem is not None:
            pars['__FINALIZER_SAMPLE__/__Chemical_formula__'] = str(chem)
        
        # Set shell and FOM parameters
        pars['__FINALIZER_FILTERS_AND_LIMITS__/__Apply_printout_options__'] = '1'
        pars['__FINALIZER_FILTERS_AND_LIMITS__/__Printout_options_-_number_of_shells__'] = str(N_shells)
        
        for i, fom_name in enumerate(fom):
            fom_idx = _FOM_DICT.get(fom_name, 0)
            pars[f'__FINALIZER_FILTERS_AND_LIMITS__/__Printout_options_-_Output_order_-_{i}__'] = str(fom_idx)
        
        # Apply all parameters
        for xpath, value in pars.items():
            elem = root.find(xpath)
            if elem is not None:
                elem.text = value
            else:
                warnings.warn(f'XML path not found: {xpath}')
        
        self.tree = tree
        
        if self.filename:
            tree.write(self.full_path)
