def parse_cap_meta(experiments: Union[str, List[str]],
                   log_fun: Optional[Callable[[str], None]] = None,
                   include_merged: bool = False,
                   exclude: Tuple[str, ...] = ('tomo', 'DD_Calib', 'Preset', 'Cluster'),
                   include: Optional[List[str]] = None) -> List[Dict[str, Any]]:
    """Parse metadata from CrysAlisPro experiment directories.
    
    This function extracts comprehensive metadata from various files in experiment
    directories, including XML info files, queue settings, data collection parameters,
    and snapshot images.
    
    Args:
        experiments: Path(s) to experiment folder(s) or CSV file(s)
        log_fun: Logging function (default: print)
        include_merged: Include merged experiments (type 6)
        exclude: Tuple of strings to exclude from experiment names
        include: Optional list of strings that must be in experiment names
    
    Returns:
        List of experiment metadata dicts with keys:
            - path: Experiment path
            - name: Experiment name
            - digest: MD5 hash for stable identification
            - scan_range, detector_distance, indexation, e1, e2, e3: From XML
            - diff_limit, r_int: Data quality metrics
            - stage_x, stage_y, stage_z: Stage positions
            - grain_x_px, grain_y_px: Grain position in pixels
            - aperture_px: Aperture size in pixels
            - diff-<ext>, grain-<ext>, minimap-<ext>, post-<ext>: Snapshot file paths
    
    Example:
        >>> meta = parse_cap_meta('example_data/exp_11317')
        >>> for exp in meta:
        ...     print(f"{exp['name']}: R_int = {exp.get('r_int', 'N/A')}")
    """
    if isinstance(experiments, str):
        experiments = [experiments]
    
    log = print if log_fun is None else log_fun
    
    # Step 1: Collect experiment paths
    exp_list = []
    for exp_entry in experiments:
        if not os.path.exists(exp_entry):
            raise FileNotFoundError(f'Input folder or CSV file {exp_entry} does not exist')
        
        log(f'Scanning {exp_entry}...')
        if exp_entry.endswith('.csv'):
            with open(exp_entry) as fh:
                for _ in range(7):
                    _ = fh.readline()
                ds = list(csv.DictReader(fh))
                new_exp = [os.path.join(d['Dataset path'], d['Experiment name']) for d in ds]
                log(f'Found {len(new_exp)} experiments in CSV')
        else:
            new_exp = [os.path.splitext(fn)[0] for fn in 
                      glob(os.path.join(exp_entry, '**\\*.par'), recursive=True)
                      if '_cracker' not in fn]
            log(f'Found {len(new_exp)} .par files')
        
        exp_list.extend(new_exp)
    
    # Filter experiments
    N = len(exp_list)
    exp_list = [fn for fn in exp_list if all(expr not in fn for expr in exclude)]
    log(f'{N - len(exp_list)} experiments excluded by exclusion list')
    
    if include is not None:
        N = len(exp_list)
        exp_list = [fn for fn in exp_list if any(expr in fn for expr in include)]
        log(f'{N - len(exp_list)} experiments excluded by inclusion list')
    
    exp_list = sorted(list(set(exp_list)))
    log(f'Processing {len(exp_list)} experiments')
    
    # Step 2: Extract metadata
    info = []
    
    for exp in exp_list:
        exp_info = _parse_experiment_metadata(exp, log, include_merged)
        if exp_info is not None:
            info.append(exp_info)
    
    return info


def _parse_experiment_metadata(exp: str, log: Callable,
                               include_merged: bool) -> Optional[Dict[str, Any]]:
    """Parse metadata for a single experiment (helper function)."""
    exp_dir = os.path.dirname(exp)
    
    # Parse XML info file
    info_fn = os.path.join(exp_dir, 'experiment_results.xmlinfo')
    if not os.path.exists(info_fn):
        log(f'WARNING: {info_fn} missing. Skipping.')
        return None
    
    try:
        with open(info_fn) as f:
            info_xml_str = f.read()
        tree = ET.fromstring('<root>\n' + info_xml_str + '\n</root>')
    except Exception as e:
        log(f'ERROR parsing {info_fn}: {e}')
        return None
    
    exp_info = {'path': exp}
    
    # Check experiment type
    if (exp_type := tree.find('__EXPERIMENT_INFO__/__EXPERIMENT_TYPE__')) is not None:
        if exp_type.text and float(exp_type.text) == 6.0 and not include_merged:
            log(f'Skipping merged experiment {exp}')
            return None
    
    # Extract basic info for digest
    user = 'anonymous'
    exp_time = 'unknown time'
    exp_name = os.path.basename(exp)
    
    if (user_elem := tree.find('__EXPERIMENT_INFO__/__USER__')) is not None:
        user = user_elem.text or 'anonymous'
    if (time_elem := tree.find('__EXPERIMENT_INFO__/__START_TIME__')) is not None:
        exp_time = time_elem.text or 'unknown time'
    if (name_elem := tree.find('__EXPERIMENT_INFO__/__EXPERIMENT_PAR_NAME_WOEXT__')) is not None:
        exp_name = name_elem.text or os.path.basename(exp)
    
    exp_info['name'] = exp_name
    
    # Extract XML entries
    xml_fields = {
        'scan_range': '__EXPERIMENT_INFO__/__SCAN_RANGE__',
        'detector_distance': '__EXPERIMENT_INFO__/__DETECTOR_DISTANCE__',
        'indexation': '__EXPERIMENT_RESULTS__/__INDEXATION__',
        'e1': '__EXPERIMENT_RESULTS__/__MOSAICITY__/__MOSAICITY_E1__',
        'e2': '__EXPERIMENT_RESULTS__/__MOSAICITY__/__MOSAICITY_E2__',
        'e3': '__EXPERIMENT_RESULTS__/__MOSAICITY__/__MOSAICITY_E3__',
        'diff_limit': '__EXPERIMENT_RESULTS__/__DIFFLIMIT__',
        'r_int': '__EXPERIMENT_RESULTS__/__RINT__',
        'stage_x': '__EXPERIMENT_INFO__/__STAGE_POSITION__/__STAGE_POSITION_X__',
        'stage_y': '__EXPERIMENT_INFO__/__STAGE_POSITION__/__STAGE_POSITION_Y__',
        'stage_z': '__EXPERIMENT_INFO__/__STAGE_POSITION__/__STAGE_POSITION_Z__',
    }
    
    for key, xpath in xml_fields.items():
        elem = tree.find(xpath)
        if elem is not None and elem.text:
            try:
                exp_info[key] = float(elem.text)
            except ValueError:
                pass
    
    # Generate stable digest
    m = hashlib.md5()
    m.update(f'{user};{exp_time};{exp_name}'.encode())
    exp_info['digest'] = m.hexdigest()
    
    # Parse queue info file
    xpos, ypos = 387.5, 192.5
    qed_file = os.path.join(exp_dir, 'metadataexpsettings.qed')
    if os.path.exists(qed_file):
        try:
            with open(qed_file, 'r') as fh:
                for ln in fh:
                    if 'la_abspixelrequestedposition_xy2[0]' in ln:
                        xpos = int(ln.split('=')[1].strip())
                    elif 'la_abspixelrequestedposition_xy2[1]' in ln:
                        ypos = int(ln.split('=')[1].strip())
        except Exception:
            pass
    
    exp_info['grain_x_px'] = xpos
    exp_info['grain_y_px'] = ypos
    
    # Parse data collection ini file
    try:
        config = configparser.ConfigParser()
        ini_path = os.path.join(exp_dir, 'expinfo', f'{exp_name}_datacoll.ini')
        config.read(ini_path)
        
        OL_demag, visual_pxs = 47, 0.036
        aperture_info = config['MicroED'].get('Aperture SA info', None)
        if aperture_info:
            exp_info['aperture_px'] = float(aperture_info) / OL_demag / visual_pxs
        else:
            exp_info['aperture_px'] = -1
    except Exception:
        exp_info['aperture_px'] = -1
    
    # Find snapshot files
    extensions = ['rodhypix', 'jpg', 'tiff']
    kinds = {
        'diff': 'middle_microed_diff_snapshot',
        'grain': 'microed_grain_snapshot',
        'minimap': 'microed_minimap_snapshot',
        'post': 'microed_post_snapshot'
    }
    
    for kind, fn_label in kinds.items():
        for ext in extensions:
            fn = os.path.join(exp_dir, f'{exp_name}_{fn_label}.{ext}')
            if os.path.exists(fn):
                exp_info[f'{kind}-{ext}'] = fn
    
    return exp_info


